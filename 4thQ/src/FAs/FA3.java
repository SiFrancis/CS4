    private void saveButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveButtonActionPerformed
        try {
            FileWriter fw = new FileWriter(saveField.getText()+".txt");
            fw.write("Min: " + minField.getText()+"\n");
            fw.write("Max: " + maxField.getText()+"\n");
            fw.write("Out: " + outLabel.getText()+"\n");
            fw.close();
	    promptLabel.setText("File saved!");
        } catch (Exception e) {
	    promptLabel.setText("Saving error.");
            System.out.println("Quit due to saving error.");
        }
    }//GEN-LAST:event_saveButtonActionPerformed

