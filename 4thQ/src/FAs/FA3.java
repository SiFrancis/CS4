    private void loadButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loadButtonActionPerformed
        try {
            File f = new File(saveField.getText()+".txt");
            Scanner scan = new Scanner(f);
            minField.setText(scan.nextLine().substring(5));
            maxField.setText(scan.nextLine().substring(5));
            outLabel.setText(scan.nextLine().substring(5));
            scan.close();
            promptLabel.setText("File loaded!");
        } catch (Exception e) {
            promptLabel.setText("Loading error.");
            System.out.println("Quit due to loading error.");
        }
    }//GEN-LAST:event_loadButtonActionPerformed

