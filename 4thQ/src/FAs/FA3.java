    private void genButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_genButtonActionPerformed
        int min = Integer.parseInt(minField.getText());
        int max = Integer.parseInt(maxField.getText());
        int dummy;
        try {
            if (min > max) {dummy = min; min = max; max = dummy;}
            outLabel.setForeground(Color.black);
            outLabel.setText(String.valueOf(rand.nextInt(max - min + 1) + min));
        } catch (Exception e) {
            outLabel.setForeground(Color.red);
            outLabel.setText("ERROR");
            minField.setText("");
            maxField.setText("");
        }
    }//GEN-LAST:event_genButtonActionPerformed

